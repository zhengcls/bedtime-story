VOICE = "zh-CN-XiaoyiNeural"

async def gen(idx, text):
    out = f"audio/ch{idx}.mp3"
    communicate = edge_tts.Communicate(text, VOICE, rate="-10%", pitch="+0Hz")
    await communicate.save(out)
    print(f"ch{idx}.mp3 done")

async def main():
    tasks = [gen(ch["idx"], ch["text"]) for ch in chapters]
    await asyncio.gather(*tasks)
    print("all done")

asyncio.run(main())
