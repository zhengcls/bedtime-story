    (3, "小蚯蚓听说了这件事，悄悄地钻进泥土里帮慢慢松土。小蜜蜂也飞来飞去，用露珠帮慢慢浇水。大家都喜欢慢慢，愿意一起照顾那颗珍贵的小种子。"),
    (4, "慢慢每天都来看望种子，用温柔的声音唱歌给它听，讲美丽的故事给它听。一天又一天，泥土里终于顶出了一点点嫩绿，小小的种子发芽了！慢慢高兴地跳起舞来。"),
