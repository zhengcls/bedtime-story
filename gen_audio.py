async def gen(ch, text):
    communicate = edge_tts.Communicate(text, "zh-CN-XiaoyiNeural", rate="-10%", volume="+5%")
    await communicate.save(f"audio/ch{ch}.mp3")
    print(f"ch{ch}.mp3 生成完毕")

async def main():
    for ch, text in chapters:
        await gen(ch, text)

asyncio.run(main())
print("全部音频生成完成！")
